# Define the penetration levels to evaluate. Modify these to change the scenario outputs.
SCENARIOS = [150, 200, 250]
SCENARIO_LABELS = ['150 EVs (Low Stress)', '200 EVs (Medium Stress)', '250 EVs (High Stress)']

